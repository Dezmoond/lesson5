balance = 0
purchase_history = []


def display_menu():
    print("1. Пополнение счета")
    print("2. Покупка")
    print("3. История покупок")
    print("4. Выход")


def top_up_account():
    global balance
    amount = float(input("Введите сумму для пополнения счета: "))
    balance += amount
    print(f"Счет пополнен на {amount} рублей. Текущий баланс: {balance} рублей.")


def make_purchase():
    global balance, purchase_history
    purchase_amount = float(input("Введите сумму покупки: "))
    if purchase_amount > balance:
        print("Денег не хватает для этой покупки.")
    else:
        item_name = input("Введите название покупки: ")
        balance -= purchase_amount
        purchase_history.append((item_name, purchase_amount))
        print(f"Покупка '{item_name}' на сумму {purchase_amount} рублей. Текущий баланс: {balance} рублей.")


def view_purchase_history():
    if not purchase_history:
        print("История покупок пуста.")
    else:
        print("История покупок:")
        for item, amount in purchase_history:
            print(f"Товар: {item}, Сумма: {amount} рублей.")

def bank():
# Главный цикл программы
    while True:
        display_menu()
        choice = input("Выберите пункт меню: ")

        if choice == '1':
            top_up_account()
        elif choice == '2':
            make_purchase()
        elif choice == '3':
            view_purchase_history()
        elif choice == '4':
            print("Выход из программы.")
            break
        else:
            print("Неверный пункт меню, попробуйте снова.")